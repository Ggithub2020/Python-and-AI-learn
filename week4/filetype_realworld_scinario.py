# Real-world example: Writing log data after processing
processed_users = ["Alice", "Bob", "Charlie"]
with open("logfile.txt", "w") as log:
    log.write("User Data Processing Log\n")
    log.write("========================\n")
    for user in processed_users:
        log.write(f"Processed user: {user}\n")
    log.write("All users processed successfully.\n")
    log.write("Log generated by: https://www.example.com/app\n")